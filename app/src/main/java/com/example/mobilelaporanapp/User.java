package com.example.mobilelaporanapp;

public class User {
    private String _id;
    private String name;
    private String email;
    private String phone;

    // Getter methods
    public String getId() {
        return _id;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }
}
